package com.qwer.fapp.conpany;

public class ConpanyDto {
	
	private String seq;
	private String conpany;
	private String affiliation;
	private String position;
	private Integer main;
	private Integer contactMain_seq;
//	-----
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getConpany() {
		return conpany;
	}
	public void setConpany(String conpany) {
		this.conpany = conpany;
	}
	public String getAffiliation() {
		return affiliation;
	}
	public void setAffiliation(String affiliation) {
		this.affiliation = affiliation;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public Integer getMain() {
		return main;
	}
	public void setMain(Integer main) {
		this.main = main;
	}
	public Integer getContactMain_seq() {
		return contactMain_seq;
	}
	public void setContactMain_seq(Integer contactMain_seq) {
		this.contactMain_seq = contactMain_seq;
	}
	
	

}
